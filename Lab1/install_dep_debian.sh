# Install dependencies for SFML on Debian-based systems
# Tested on Ubuntu 20.04 and later
sudo apt-get update
sudo apt-get install libudev-dev
sudo apt-get install libopenal-dev
sudo apt-get install libvorbis-dev libogg-dev
sudo apt-get install libflac-dev
sudo apt install libx11-dev libxrandr-dev libxcursor-dev libxi-dev libxinerama-dev libgl1-mesa-dev libglu1-mesa-dev
sudo apt install libfreetype6-dev